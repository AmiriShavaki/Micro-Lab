void disp(int x, int y) {
    int i = 0;           
    lcd_clear();
    while (name[i]) {
        lcd_gotoxy(x, y);
        lcd_putchar(name[i++]);
        if (x < 15) {
            x++;
        } else {
            x = 0;
            y = 1 - y;
        }
    }
}

